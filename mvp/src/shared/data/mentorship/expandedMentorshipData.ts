// Utility functions for filtering mentors
export const getMentorsByCategory = (categoryId: string, limit: number = 10): Mentor[] => {
  const mentors = expandedMentorData.filter(mentor => mentor.categoryId === categoryId);
  return mentors.slice(0, limit);
};

export const getAllMentorsByCategories = (categoryIds: string[]): Mentor[] => {
  if (categoryIds.length === 0) return expandedMentorData;
  
  const mentors = expandedMentorData.filter(mentor => 
    categoryIds.includes(mentor.categoryId)
  );
  
  // Group by category and limit each category to 10 mentors
  const mentorsByCategory: { [key: string]: Mentor[] } = {};
  
  mentors.forEach(mentor => {
    if (!mentorsByCategory[mentor.categoryId]) {
      mentorsByCategory[mentor.categoryId] = [];
    }
    if (mentorsByCategory[mentor.categoryId].length < 10) {
      mentorsByCategory[mentor.categoryId].push(mentor);
    }
  });
  
  // Flatten the result
  return Object.values(mentorsByCategory).flat();
};

export const getMentorStatsByCategory = (categoryId: string) => {
  const mentors = getMentorsByCategory(categoryId);
  return {
    totalMentors: mentors.length,
    averageRating: mentors.reduce((sum, mentor) => sum + mentor.rating, 0) / mentors.length,
    averagePrice: mentors.reduce((sum, mentor) => sum + mentor.price, 0) / mentors.length,
    totalReviews: mentors.reduce((sum, mentor) => sum + mentor.reviews, 0)
  };
};
